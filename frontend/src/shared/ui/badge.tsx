import * as React from 'react';
import { Slot } from '@radix-ui/react-slot';
import { cva, type VariantProps } from 'class-variance-authority';

import { cn } from '@/shared/lib/utils';

const badgeVariants = cva(
  'inline-flex items-center justify-center rounded-full border px-2 py-0.5 text-xs font-medium w-fit whitespace-nowrap shrink-0 [&>svg]:size-3 gap-1 [&>svg]:pointer-events-none focus-visible:border-ring focus-visible:ring-ring/50 focus-visible:ring-[3px] aria-invalid:ring-destructive/20 dark:aria-invalid:ring-destructive/40 aria-invalid:border-destructive transition-[color,box-shadow] overflow-hidden',
  {
    variants: {
      variant: {
        default:
          'border-transparent bg-primary text-primary-foreground [a&]:hover:bg-primary/90',

        warn: 'border-transparent [a&]:hover:bg-yellow-100/90 text-yellow-700 bg-yellow-100',
        info: 'border-transparent [a&]:hover:bg-blue-100/90 text-blue-700 bg-blue-100',
        'info-secondary':
          'border-transparent [a&]:hover:bg-purple-100/90 text-purple-700 bg-purple-100',

        success:
          'border-transparent [a&]:hover:bg-green-100/90 text-green-700 bg-green-100',

        'destructive-secondary':
          'border-transparent [a&]:hover:bg-red-100/90 text-red-700 bg-red-100',

        'warn-outline':
          'border-yellow-500 text-yellow-600 bg-transparent [a&]:hover:bg-yellow-50',
        'info-outline':
          'border-blue-500 text-blue-700 bg-transparent [a&]:hover:bg-blue-50',
        'info-secondary-outline':
          'border-purple-500 text-purple-700 bg-transparent [a&]:hover:bg-purple-50',
        'success-outline':
          'border-green-400 text-green-600 bg-transparent [a&]:hover:bg-green-50',
        'destructive-outline':
          'border-destructive/60 text-destructive bg-transparent [a&]:hover:bg-destructive/5 focus-visible:ring-destructive/20 dark:focus-visible:ring-destructive/40',

        secondary:
          'border-transparent bg-secondary text-secondary-foreground [a&]:hover:bg-secondary/90',
        destructive:
          'border-transparent bg-destructive text-white [a&]:hover:bg-destructive/90 focus-visible:ring-destructive/20 dark:focus-visible:ring-destructive/40 dark:bg-destructive/60',
        outline:
          'text-foreground [a&]:hover:bg-accent [a&]:hover:text-accent-foreground',
      },
    },
    defaultVariants: {
      variant: 'default',
    },
  }
);

export type BadgeVariants = VariantProps<typeof badgeVariants>;
export type BadgeProps = React.ComponentProps<'span'> &
  BadgeVariants & { asChild?: boolean };

function Badge({ className, variant, asChild = false, ...props }: BadgeProps) {
  const Comp = asChild ? Slot : 'span';

  return (
    <Comp
      data-slot='badge'
      className={cn(badgeVariants({ variant }), className)}
      {...props}
    />
  );
}

export { Badge, badgeVariants };
